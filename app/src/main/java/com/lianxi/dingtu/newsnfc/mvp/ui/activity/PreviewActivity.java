public class PreviewActivity extends BaseActivity<PreviewPresenter> implements PreviewContract.View {
    @BindView(R.id.image_view) ImageView imageView;
    private String image_url;

    @Override
    public void setupActivityComponent(@NonNull AppComponent appComponent) {
        DaggerPreviewComponent //如找不到该类,请编译一下项目
                .builder()
                .appComponent(appComponent)
                .view(this)
                .build()
                .inject(this);
    }

    @Override
    public int initView(@Nullable Bundle savedInstanceState) {
        return R.layout.activity_preview; //如果你不需要框架帮你设置 setContentView(id) 需要自行设置,请返回 0
    }

    @Override
    public void initData(@Nullable Bundle savedInstanceState) {
        this.setTitle("图片预览");
        image_url = getIntent().getStringExtra("startWithUri");
        if (!TextUtils.isEmpty(image_url))
            GlideArms.with(this).load(image_url)
                    .apply(RequestOptions.skipMemoryCacheOf(true))
                    .apply(RequestOptions.diskCacheStrategyOf(DiskCacheStrategy.NONE))
                    .into(imageView);
    }

    @Override
    public void showLoading() {

    }

    @Override
    public void hideLoading() {

    }

    @Override
    public void showMessage(@NonNull String message) {
        checkNotNull(message);
        ArmsUtils.snackbarText(message);
    }

    @Override
    public void launchActivity(@NonNull Intent intent) {
        checkNotNull(intent);
        ArmsUtils.startActivity(intent);
    }

    @Override
    public void killMyself() {
        finish();
    }

    @OnClick(R.id.submit) public void onViewClicked() {
        EventBus.getDefault().post(new UploadImageEvent(image_url));
        finish();
    }
}
